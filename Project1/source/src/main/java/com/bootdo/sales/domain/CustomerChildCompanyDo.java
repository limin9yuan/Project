package com.bootdo.sales.domain;

import java.io.Serializable;
import java.util.Date;

//客户组织机构_子公司
public class CustomerChildCompanyDo implements Serializable{
	private static final long serialVersionUID = 1L;
	
	//子公司编号
	private String childCompanyId;
	//企业客户编号
	private String customerId;
	//子公司名称
	private String childCompanyName;
	//备注
	private String childCompanyRemarks;
	//操作人
	private String childCompanyOperator;
	//修改时间
	private String childCompanyOperateTime;
	//创建时间
	private Date childCompanyCreateTime;
	
	public String getChildCompanyId() {
		return childCompanyId;
	}
	public void setChildCompanyId(String childCompanyId) {
		this.childCompanyId = childCompanyId;
	}
	public String getCustomerId() {
		return customerId;
	}
	public void setCustomerId(String customerId) {
		this.customerId = customerId;
	}
	public String getChildCompanyName() {
		return childCompanyName;
	}
	public void setChildCompanyName(String childCompanyName) {
		this.childCompanyName = childCompanyName;
	}
	public String getChildCompanyRemarks() {
		return childCompanyRemarks;
	}
	public void setChildCompanyRemarks(String childCompanyRemarks) {
		this.childCompanyRemarks = childCompanyRemarks;
	}
	public String getChildCompanyOperator() {
		return childCompanyOperator;
	}
	public void setChildCompanyOperator(String childCompanyOperator) {
		this.childCompanyOperator = childCompanyOperator;
	}
	public String getChildCompanyOperateTime() {
		return childCompanyOperateTime;
	}
	public void setChildCompanyOperateTime(String childCompanyOperateTime) {
		this.childCompanyOperateTime = childCompanyOperateTime;
	}
	public Date getChildCompanyCreateTime() {
		return childCompanyCreateTime;
	}
	public void setChildCompanyCreateTime(Date childCompanyCreateTime) {
		this.childCompanyCreateTime = childCompanyCreateTime;
	}
	
	

}
